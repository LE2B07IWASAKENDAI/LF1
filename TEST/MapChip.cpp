void MapChip::Draw()
{
	//キー押下でマップ番号変更
	if (CheckHitKey(KEY_INPUT_L) == 1) {
		//マップ番号の指定
		mapNumber = 0;
	}
	if (CheckHitKey(KEY_INPUT_P) == 1) {
		mapNumber = 1;
	}
	//現在のマップ番号更新
	UpdateMapNumber(GetMapNumber());
	
	MapDraw();
}

